void Player::moveForward() {
	deltaTime = (float)glfwGetTime() - lastFrame;
	lastFrame = (float)glfwGetTime();
	time += deltaTime;

	if (time > movementSpeed) {
		frontVec = currentFrontVec;
		pos += frontVec;

		if (pos.x < -7.5f) {
			pos.x = 8.5f;
		}
		if (pos.x > 8.5f) {
			pos.x = -7.5f;
		}
		if (pos.z < -7.5f) {
			pos.z = 8.5f;
		}
		if (pos.z > 8.5f) {
			pos.z = -7.5f;
		}
		time = 0.0;

		if (!segments.empty()) {
			for (int i = segments.size() - 1; i >= 0; i--) {
				if (i == 0) {
					segments[i].pos = pos;
				}
				else {
					segments[i].pos = segments[i - 1].pos;
				}
			}
		}
	}
}

void Player::updateSpeed() {
	switch (points) {
	case 0:
		movementSpeed = 0.3f;
		break;
	case 5:
		movementSpeed = 0.225f;
		break;
	case 10:
		movementSpeed = 0.15f;
		break;
	case 15:
		movementSpeed = 0.1f;
		break;
	default:
		break;
	}
}